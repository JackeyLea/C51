;                  二进制加法试验												*
; p2口八个灯作二进制加法。理解二进值的计算                                  	*
; 硬件连接：p2口8路指示灯														*
;																				*
;*  描述:                                                                       *
;   p2口八个灯作二进制加法。理解二进值的计算                                    *
;*  连接方法： JP11（P2）和J12(LED灯) 用8PIN排线连接起来                        *
;********************************************************************************

     	ORG 	0000h	 ;程序从0000开始运行
     	AJMP	main
	    ORG	    0030h
main:	MOV	A  ,#0ffh
loop: 	MOV     p2,A	;全不亮
    	CALL    delay	;延时。
	    DEC	    A	    ;每次减一。
	    JMP     loop

delay:	MOV r5,#50	;延时。
d1: 	MOV r6,#40
d2:	MOV r7,#248
	DJNZ r7,$
	DJNZ r6,d2
	DJNZ r5,d1
	RET
END
